def _qn(tag: str) -> str:
    prefix, local = tag.split(":")
    ns = {"w": W_NS}
    return f"{{{ns[prefix]}}}{local}"


def get_visible_paragraphs(body) -> list:
    """Extract visible text per paragraph (excluding deleted runs)."""
    result = []
    for para in body.iter(_qn("w:p")):
        texts = []
        for t in para.iter(_qn("w:t")):
            # Skip if inside a w:del element
            parent = t.getparent()
            in_del = False
            while parent is not None:
                if parent.tag == _qn("w:del"):
                    in_del = True
                    break
                parent = parent.getparent()
            if not in_del:
                texts.append(t.text or "")
        text = "".join(texts).strip()
        if text:
            result.append(text)
    return result


def get_header_visible_text(doc) -> list:
    """Extract visible header paragraphs."""
    result = []
    for section in doc.sections:
        h = section.header
        if not h:
            continue
        for para in h._element.iter(_qn("w:p")):
            texts = []
            for t in para.iter(_qn("w:t")):
                parent = t.getparent()
                in_del = False
                while parent is not None:
                    if parent.tag == _qn("w:del"):
                        in_del = True
                        break
                    parent = parent.getparent()
                if not in_del:
                    texts.append(t.text or "")
            text = "".join(texts).strip()
            if text:
                result.append(text)
    return result


def strip_html(s: str) -> str:
    """Strip HTML tags and decode entities for comparison."""
    s = re.sub(r'<[^>]+>', '', s)
    s = html.unescape(s)
    s = re.sub(r'\s+', ' ', s).strip()
    return s


def fmt_period(value: int, unit: str = "days") -> str:
    return f"{convert_to_words(value).lower()} ({value}) {unit}"


def fmt_period_val(value: int) -> str:
    """Format period value WITHOUT unit (matching document_generator._format_period)."""
    return f"{convert_to_words(value).lower()} ({value})"


def fmt_dollar(amount: float) -> str:
    return to_legal_dollar_string(amount)


def normalize(s: str) -> str:
    """Normalize text for comparison: collapse whitespace, strip."""
    return re.sub(r'\s+', ' ', s).strip()


def strip_letter_prefix(s: str) -> str:
    """Strip auto-numbered letter prefixes like 'A. ', 'B. ' etc.
    These are auto-generated by Word's StandardL1 style numbering
    and don't appear in w:t text."""
    return re.sub(r'^[A-L]\.\s+', '', s)


