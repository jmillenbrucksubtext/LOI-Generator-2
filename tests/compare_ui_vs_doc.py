def run_comparison(form: LoiFormData, scenario_name: str):
    print(f"\n{'='*70}")
    print(f"  SCENARIO: {scenario_name}")
    print(f"{'='*70}")

    gen = DocumentGenerator()
    buf = gen.generate(TEMPLATE_PATH, form)
    doc = Document(buf)
    body = doc.element.body

    doc_paragraphs = get_visible_paragraphs(body)
    doc_headers = get_header_visible_text(doc)
    ui_lines = build_ui_preview_text(form)

    # Normalize all for comparison
    doc_normalized = [normalize(p) for p in doc_paragraphs if normalize(p)]
    ui_normalized = [normalize(l) for l in ui_lines if normalize(l)]

    # Strip auto-numbered letter prefixes from UI text for comparison.
    # The Word template uses StandardL1 style with upperLetter numbering,
    # which auto-generates "A.", "B.", etc. These appear in Word but
    # aren't in w:t text nodes.
    ui_stripped = [strip_letter_prefix(t) for t in ui_normalized]

    discrepancies = []

    # Check each UI line exists in the document
    for i, ui_text in enumerate(ui_stripped):
        found = False
        for doc_text in doc_normalized:
            # Check if the UI text is contained in (or matches) a doc paragraph
            if ui_text in doc_text or doc_text in ui_text:
                found = True
                # Check for exact match
                if ui_text != doc_text:
                    # Find the difference
                    if len(ui_text) > 50 and len(doc_text) > 50:
                        # Long text - find first difference
                        min_len = min(len(ui_text), len(doc_text))
                        diff_pos = next((j for j in range(min_len) if ui_text[j] != doc_text[j]), min_len)
                        context_start = max(0, diff_pos - 30)
                        context_end = min(max(len(ui_text), len(doc_text)), diff_pos + 50)
                        discrepancies.append({
                            "type": "TEXT_MISMATCH",
                            "ui_section": ui_text[:80] + "...",
                            "diff_pos": diff_pos,
                            "ui_context": ui_text[context_start:context_end],
                            "doc_context": doc_text[context_start:min(context_end, len(doc_text))],
                        })
                break

        if not found:
            # Check headers too
            for h_text in doc_headers:
                h_norm = normalize(h_text)
                if ui_text in h_norm or h_norm in ui_text:
                    found = True
                    break

        if not found:
            discrepancies.append({
                "type": "UI_ONLY",
                "text": ui_text[:120],
            })

    # Check each doc paragraph exists in UI
    for doc_text in doc_normalized:
        found = False
        for ui_text in ui_stripped:
            if doc_text in ui_text or ui_text in doc_text:
                found = True
                break
        # Also check headers
        if not found:
            for h_text in doc_headers:
                if doc_text in normalize(h_text) or normalize(h_text) in doc_text:
                    found = True
                    break
        if not found:
            # Skip signature underlines and blank-ish content
            if doc_text.startswith("____") or doc_text in ("By:", "Name:", "Title:"):
                continue
            discrepancies.append({
                "type": "DOC_ONLY",
                "text": doc_text[:120],
            })

    if discrepancies:
        print(f"\n  Found {len(discrepancies)} discrepancies:\n")
        for d in discrepancies:
            if d["type"] == "UI_ONLY":
                print(f"  [UI ONLY] Text in preview but not in document:")
                print(f"    \"{d['text']}\"")
            elif d["type"] == "DOC_ONLY":
                print(f"  [DOC ONLY] Text in document but not in preview:")
                print(f"    \"{d['text']}\"")
            elif d["type"] == "TEXT_MISMATCH":
                print(f"  [MISMATCH] Text differs at position {d['diff_pos']}:")
                print(f"    UI:  \"{d['ui_context']}\"")
                print(f"    DOC: \"{d['doc_context']}\"")
            print()
    else:
        print("\n  No discrepancies found!")

    return len(discrepancies)


